public class WeatherEntity {
	private String datetime;// 日期
	private String direction;// 风向
	private String speed;// 风速
	private String tempe;// 温度
	private String weather;// 天气
	private String weatherpic;// 天气图片
	private String pic;// 图片路径
	private String date;// 日期
	private String week;// 星期
	private String createtime; // 创建时间
	private String remark;// 备注

	public String getDatetime() {
		return datetime;
	}

	public void setDatetime(String datetime) {
		this.datetime = datetime;
	}

	public String getDirection() {
		return direction;
	}

	public void setDirection(String direction) {
		this.direction = direction;
	}

	public String getSpeed() {
		return speed;
	}

	public void setSpeed(String speed) {
		this.speed = speed;
	}

	public String getTempe() {
		return tempe;
	}

	public void setTempe(String tempe) {
		this.tempe = tempe;
	}

	public String getWeather() {
		return weather;
	}

	public void setWeather(String weather) {
		this.weather = weather;
	}

	public String getWeatherpic() {
		return weatherpic;
	}

	public void setWeatherpic(String weatherpic) {
		this.weatherpic = weatherpic;
	}

	public String getPic() {
		return pic;
	}

	public void setPic(String pic) {
		this.pic = pic;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getWeek() {
		return week;
	}

	public void setWeek(String week) {
		this.week = week;
	}

	public String getCreatetime() {
		return createtime;
	}

	public void setCreatetime(String createtime) {
		this.createtime = createtime;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

}
